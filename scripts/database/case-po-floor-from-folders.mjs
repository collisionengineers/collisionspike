#!/usr/bin/env node
/**
 * Offline Case/PO sequence-floor seed generator governed by ADR-0022.
 *
 * INPUT:  a text file of Box ARCHIVE folder names, one per line (from a Box
 *         listing export, or the facade's box/search + folders/{id}/items).
 * OUTPUT: idempotent `INSERT ... ON CONFLICT DO UPDATE (GREATEST)` statements for
 *         `case_po_floor` on stdout (pipe into the psql session of the postgres.md
 *         runbook), plus a human summary on stderr. Non-Case/PO names are listed
 *         on stderr so naming variants (e.g. "CCPY26050 - Smith") are VISIBLE,
 *         never silently dropped.
 *
 *   node scripts/database/case-po-floor-from-folders.mjs folder-names.txt [--year 26]
 *
 * OFFLINE + read-only: this script talks to nothing — it turns a listing you
 * already have into SQL you review before applying.
 */

import { readFileSync } from 'node:fs';

const args = process.argv.slice(2);
const file = args.find((a) => !a.startsWith('--'));
const yearArg = (() => {
  const i = args.indexOf('--year');
  return i >= 0 ? args[i + 1] : undefined;
})();
if (!file) {
  console.error('usage: node case-po-floor-from-folders.mjs <folder-names.txt> [--year YY]');
  process.exit(2);
}

// Marker + shape rules mirror @cs/domain (CASE_PO_SHAPE_RE / parseCasePoMarker):
// optional AP./A./D. marker (longest first), leading-alpha principal run, 2-digit
// year, 3-4 digit sequence. Anchored full-match — a suffixed folder name is a
// naming VARIANT to report, not to guess at.
const MARKERS = ['AP.', 'A.', 'D.'];
function parseCasePoName(raw) {
  const token = raw.trim().toUpperCase().replace(/^((?:AP|A|D)\.)\s+/, '$1');
  if (!token) return null;
  let marker = '';
  let body = token;
  for (const m of MARKERS) {
    if (body.startsWith(m)) {
      marker = m;
      body = body.slice(m.length);
      break;
    }
  }
  const m = body.match(/^([A-Z]{2,8})(\d{2})(\d{3,4})$/);
  if (!m) return null;
  return { marker, principal: m[1], yy: m[2], seq: Number(m[3]) };
}

const lines = readFileSync(file, 'utf8').split(/\r?\n/);
const floors = new Map(); // prefix -> max seq
const skipped = [];
let parsed = 0;
for (const line of lines) {
  const name = line.trim();
  if (!name) continue;
  const po = parseCasePoName(name);
  if (!po) {
    skipped.push(name);
    continue;
  }
  if (yearArg && po.yy !== yearArg) continue;
  parsed += 1;
  const prefix = `${po.marker}${po.principal}${po.yy}`;
  floors.set(prefix, Math.max(floors.get(prefix) ?? 0, po.seq));
}

if (floors.size === 0) {
  console.error(`no Case/PO-shaped folder names found in ${file} (${skipped.length} skipped)`);
  process.exit(1);
}

const today = new Date().toISOString().slice(0, 10);
console.log('-- case_po_floor seed generated by scripts/database/case-po-floor-from-folders.mjs');
console.log(`-- source: ${file} (${parsed} Case/PO folder names; ${skipped.length} non-matching skipped)`);
console.log('BEGIN;');
for (const [prefix, seq] of [...floors.entries()].sort(([a], [b]) => a.localeCompare(b))) {
  console.log(
    `INSERT INTO case_po_floor (prefix, floor_seq, note) VALUES ('${prefix}', ${seq}, 'archive scan ${today}')` +
      ` ON CONFLICT (prefix) DO UPDATE SET floor_seq = GREATEST(case_po_floor.floor_seq, EXCLUDED.floor_seq),` +
      ` note = EXCLUDED.note, updated_at = now();`,
  );
}
console.log('COMMIT;');
console.log('-- VERIFY: SELECT prefix, floor_seq FROM case_po_floor ORDER BY prefix;');

console.error(`\nfloors (${floors.size} prefixes):`);
for (const [prefix, seq] of [...floors.entries()].sort(([a], [b]) => a.localeCompare(b))) {
  console.error(`  ${prefix} -> ${seq}`);
}
if (skipped.length) {
  console.error(`\nNOT Case/PO-shaped (${skipped.length}) — review for naming variants:`);
  for (const s of skipped.slice(0, 40)) console.error(`  ${s}`);
  if (skipped.length > 40) console.error(`  … +${skipped.length - 40} more`);
}
