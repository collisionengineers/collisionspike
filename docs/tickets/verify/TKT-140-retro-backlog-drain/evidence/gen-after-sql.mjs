/**
 * TKT-140 drain phase — generate drain-after.sql: the single read-only
 * verification window run AFTER the drain (per docs/azure/postgres.md: transient
 * FW rule, Entra admin -> SET ROLE csadmin, SELECT-only, rule trap-deleted).
 * The 118 enumerated inbound row ids are baked in from enum-backlog-rows.csv so
 * the "after" state is measured over EXACTLY the dry-run's row set.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const EVID = dirname(fileURLToPath(import.meta.url));
function parseCsv(text) {
  const rows = []; let row = [], f = '', q = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (q) { if (c === '"') { if (text[i + 1] === '"') { f += '"'; i++; } else q = false; } else f += c; }
    else if (c === '"') q = true;
    else if (c === ',') { row.push(f); f = ''; }
    else if (c === '\n' || c === '\r') { if (c === '\r' && text[i + 1] === '\n') i++; row.push(f); f = ''; if (row.length > 1 || row[0] !== '') rows.push(row); row = []; }
    else f += c;
  }
  if (f !== '' || row.length > 0) { row.push(f); if (row.length > 1 || row[0] !== '') rows.push(row); }
  return rows;
}
const csv = parseCsv(readFileSync(join(EVID, 'enum-backlog-rows.csv'), 'utf8'));
const h = csv[0]; const col = (n) => h.indexOf(n);
const ids = csv.slice(1).map((r) => r[col('id')]).filter(Boolean);
if (ids.length === 0) throw new Error('no ids');
const idList = ids.map((i) => `('${i}'::uuid)`).join(',\n  ');

const drainStart = process.argv[2] ?? new Date().toISOString().slice(0, 10);

const sql = `-- ============================================================================
-- TKT-140 drain phase — AFTER-state verification (READ-ONLY; one window).
-- Generated by gen-after-sql.mjs over the dry-run's 118 enumerated row ids.
-- Drain window started: ${drainStart}
-- ============================================================================
\\timing on
SET ROLE csadmin;

\\echo '=== AFTER: inbound_email totals ==='
SELECT count(*) AS inbound_total,
       count(*) FILTER (WHERE case_id IS NULL) AS inbound_uncased
  FROM inbound_email;

\\echo '=== AFTER: the 118 enumerated dry-run rows — linkage + attention state ==='
WITH enumerated(id) AS (VALUES
  ${idList}
)
SELECT count(*)                                            AS enumerated_rows,
       count(*) FILTER (WHERE ie.case_id IS NOT NULL)      AS now_linked_to_case,
       count(*) FILTER (WHERE ie.case_id IS NULL)          AS still_uncased,
       count(*) FILTER (WHERE ie.attention_reason = 'unable_to_locate') AS stamped_unable_to_locate,
       count(*) FILTER (WHERE ie.case_id IS NULL AND ie.attention_reason IS NULL) AS uncased_unstamped
  FROM enumerated e
  JOIN inbound_email ie ON ie.id = e.id;

\\echo '=== AFTER: retro-channel cases (intake_channel_kind_code=100000003) — Held invariant check ==='
SELECT count(*) AS retro_cases,
       count(*) FILTER (WHERE status_code = (SELECT code FROM choice_case_status WHERE name = 'needs_review')) AS status_needs_review,
       count(*) FILTER (WHERE on_hold IS TRUE)      AS on_hold_true,
       count(*) FILTER (WHERE case_po IS NOT NULL)  AS with_case_po,
       count(*) FILTER (WHERE created_at >= now() - interval '12 hours') AS created_last_12h
  FROM case_
 WHERE intake_channel_kind_code = 100000003;

\\echo '=== AFTER: retro-channel cases created in the drain window (detail, newest 40) ==='
SELECT c.id, c.case_ref, c.vrm, cs.name AS status, c.on_hold, c.case_po, c.created_at
  FROM case_ c
  LEFT JOIN choice_case_status cs ON cs.code = c.status_code
 WHERE c.intake_channel_kind_code = 100000003
   AND c.created_at >= now() - interval '12 hours'
 ORDER BY c.created_at DESC
 LIMIT 40;

\\echo '=== AFTER: retro audit action totals (compare enum-retro-audit-history.csv) ==='
SELECT ca.name AS action, count(*) AS events,
       count(*) FILTER (WHERE ae.occurred_at >= now() - interval '12 hours') AS last_12h
  FROM audit_event ae
  JOIN choice_audit_action ca ON ca.code = ae.action_code
 WHERE ca.name IN ('retro_case_created', 'retro_case_linked', 'retro_reconstruction_failed')
 GROUP BY ca.name ORDER BY ca.name;

\\echo '=== AFTER: mint-guard refusals (any new since the dry-run?) ==='
SELECT ae.occurred_at, left(ae.name, 140) AS summary
  FROM audit_event ae
 WHERE ae.name LIKE 'Retro create refused%'
 ORDER BY ae.occurred_at;

RESET ROLE;
`;
writeFileSync(join(EVID, 'drain-after.sql'), sql);
console.log(`drain-after.sql written (${ids.length} enumerated row ids baked in)`);
