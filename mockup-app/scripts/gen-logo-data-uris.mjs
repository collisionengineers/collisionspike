// Generates src/assets/logos.generated.ts — brand logos as base64 data: URIs.
//
// WHY: `pac code push` corrupts binary image assets on upload (a valid PNG arrives
// ~75% larger and undecodable — UTF-8/CRLF text-mangling of the binary), and Vite's
// `?inline` is overridden by the @microsoft/power-apps plugin (assets are always
// emitted as files). So we embed the logos as TEXT (base64 ASCII) inside the JS
// bundle, which survives the deploy intact. The Code App CSP is
// `img-src 'self' data:`, so data: image URIs render. (Fonts can't use this route —
// CSP is `font-src 'self'`, which forbids data: fonts.)
//
// Run from mockup-app/:  node scripts/gen-logo-data-uris.mjs
import { readFileSync, writeFileSync } from 'node:fs';

const enc = (p) => 'data:image/png;base64,' + readFileSync(p).toString('base64');
const logoWhite = enc('src/assets/web_logo_white.png');
const logoMark = enc('src/assets/logo_no_margin.png');

const out =
  '// AUTO-GENERATED by scripts/gen-logo-data-uris.mjs — do not edit by hand.\n' +
  '// Brand logos as base64 data: URIs (pac code push corrupts binary image assets;\n' +
  "// CSP img-src 'self' data: permits data URIs). See docs/plans/phase-1-intake-and-case-tracking/code-app/logo-fix-findings.md.\n" +
  '/* eslint-disable */\n' +
  'export const logoWhite = ' + JSON.stringify(logoWhite) + ';\n' +
  'export const logoMark = ' + JSON.stringify(logoMark) + ';\n';

writeFileSync('src/assets/logos.generated.ts', out);
console.log(
  'wrote src/assets/logos.generated.ts —', out.length, 'chars (white',
  logoWhite.length, '/ mark', logoMark.length, ')'
);
